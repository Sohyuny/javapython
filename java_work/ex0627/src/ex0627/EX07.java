package ex0627;

public class EX07 {
	
	public static void main(String[] args) {
		
		int n=2;
		
		//swith 단축어 : swi + ctrl+ space key
		switch (n) {
		case 1:
			System.out.println("n==1");
			
		case 2:
			System.out.println("n==2");
			
		case 3:
			System.out.println("n==3");
			break;

		default:
			System.out.println("default");
			break;
		
		}
		
		System.out.println("끝");
		
	}

}
